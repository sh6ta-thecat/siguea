    ///Empieza rellena los datos
    public void simularDatosConDataFaker() {
        if (modeloAsistencia == null) {
            configurarTablaJTregAsis();
        }

        modeloAsistencia.setRowCount(0);
        Faker faker = new Faker(new Locale("es"));

        for (int i = 1; i <= 10; i++) {
            String num = String.valueOf(i);
            String codigo = "2026" + String.format("%04d", i);
            String apellidosNombres = faker.name().lastName()
                    + " " + faker.name().lastName()
                    + ", " + faker.name().firstName();
            String dni = faker.number().digits(8);
            String asistencia = "P";

            Object[] fila = {num, codigo, apellidosNombres, dni, asistencia};
            modeloAsistencia.addRow(fila);
        }
    }

