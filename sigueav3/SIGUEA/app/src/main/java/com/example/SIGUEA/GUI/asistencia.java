    public void configurarTablaJTregAsis() {
        String[] columnas = {"N°", "Código", "Apellidos y Nombres", "DNI", "Asistencia (P / A / T)"};

        // 1. Crear el modelo de datos
        modeloAsistencia = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Hacemos editable ÚNICAMENTE la columna de asistencia (índice 4)
                return column == 4;
            }
        };

        JTregAsis.setModel(modeloAsistencia);

        // 2. CONFIGURAR LOS ANCHOS DE LAS COLUMNAS (Para evitar el texto cortado)
        JTregAsis.getColumnModel().getColumn(0).setPreferredWidth(40);  // N°
        JTregAsis.getColumnModel().getColumn(1).setPreferredWidth(100); // Código
        JTregAsis.getColumnModel().getColumn(2).setPreferredWidth(280); // Apellidos y Nombres (Más ancho)
        JTregAsis.getColumnModel().getColumn(3).setPreferredWidth(90);  // DNI
        JTregAsis.getColumnModel().getColumn(4).setPreferredWidth(160); // Asistencia (Espacio para los botones)

        // Ajustar la altura de las filas para que quepan cómodamente los botones
        JTregAsis.setRowHeight(30);

        // 3. ASIGNAR LOS COMPONENTES DE BOTONES A LA COLUMNA 4
        AsistenciaRendererEditor renderEditor = new AsistenciaRendererEditor();
        JTregAsis.getColumnModel().getColumn(4).setCellRenderer(renderEditor);
        JTregAsis.getColumnModel().getColumn(4).setCellEditor(renderEditor);
    }

